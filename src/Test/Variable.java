package Test;

public class Variable {
    public static void main(String[] args) {
        // Declare and initialize variables with proper naming conventions

        int age = 25;                  // int: whole number
        double accountBalance = 1050.75; // double: decimal number
        boolean isLoggedIn = true;     // boolean: true/false value
        char grade = 'A';              // char: single character

        // Print the values
        System.out.println("Age: " + age);
        System.out.println("Account Balance: $" + accountBalance);
        System.out.println("Is Logged In: " + isLoggedIn);
        System.out.println("Grade: " + grade);
    }
}
